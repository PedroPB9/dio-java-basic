public class Calculator {
    /**
     * This method takes two parameters and sum them
     * @param firstNumber
     * @param secondNumber
     * @return an integer is returned, which represents the sum between the parameters
     */
    public int sum(int firstNumber, int secondNumber){
        return firstNumber + secondNumber;
    }
}
